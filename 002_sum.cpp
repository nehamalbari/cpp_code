#include <iostream> // For input/output
using namespace std; // Use standard namespace

int main() // Main function
{
    int a, b, sum; // Variables for numbers and their sum
    
    cout << "Enter the first number:"; // Prompt user
    cin >> a; // Read first number
    
    cout << "Enter the second number:"; // Prompt user
    cin >> b; // Read second number
    
    sum = a + b; // Calculate sum
    
    cout << "The sum is " << sum << endl; // Display result
    
    return 0; // Exit program
}

